"""RES-012: Missing Resource Lifetime Management.

Checks config for resource expiry/cleanup config. Fails when resources
are exposed but no TTL, expiry, or cleanup config is found.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

import yaml

from medusa.core.check import BaseCheck, ServerSnapshot
from medusa.core.models import CheckMetadata, Finding, Status

LIFETIME_KEYS: set[str] = {
    "ttl",
    "expiry",
    "expiration",
    "max_age",
    "cleanup",
    "gc",
    "garbage_collection",
    "resource_ttl",
    "resource_expiry",
    "invalidation",
    "lifecycle",
}


class ResourceLifetimeManagementCheck(BaseCheck):
    """Missing Resource Lifetime Management."""

    def metadata(self) -> CheckMetadata:
        meta_path = Path(__file__).with_suffix(".metadata.yaml")
        data = yaml.safe_load(meta_path.read_text())
        return CheckMetadata(**data)

    async def execute(self, snapshot: ServerSnapshot) -> list[Finding]:
        meta = self.metadata()
        findings: list[Finding] = []

        if not snapshot.resources:
            return findings

        has_lifetime = _walk_config_for_keys(snapshot.config_raw, LIFETIME_KEYS)

        if not has_lifetime:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.FAIL,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended=(
                        f"Server exposes {len(snapshot.resources)} resource(s) but no "
                        f"TTL, expiry, or cleanup config detected â€” resources may persist"
                        f"indefinitely."
                    ),
                    evidence="No lifetime management keys found in config_raw.",
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )
        else:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.PASS,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended="Resource lifetime management configuration detected.",
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )

        return findings


def _walk_config_for_keys(config: Any, keys: set[str], _depth: int = 0) -> bool:
    if _depth > 10:
        return False
    if isinstance(config, dict):
        for key in config:
            if isinstance(key, str) and key.lower() in keys:
                return True
            if _walk_config_for_keys(config[key], keys, _depth + 1):
                return True
    elif isinstance(config, list):
        for item in config:
            if _walk_config_for_keys(item, keys, _depth + 1):
                return True
    return False
