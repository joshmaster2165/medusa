"""IV026: Wildcard Parameter Matching.

Detects tool parameters that accept wildcard patterns (*, ?, **) without restrictions.
Unrestricted glob or wildcard parameters can match unintended files, directories, or resources,
expanding the operation scope beyond user intent.
"""

from __future__ import annotations

from pathlib import Path

import yaml

from medusa.core.check import BaseCheck, ServerSnapshot
from medusa.core.models import CheckMetadata, Finding, Status

# Patterns that are effectively wildcards -- match anything
_WILDCARD_PATTERNS: frozenset[str] = frozenset({".*", "^.*$", ".+", "^.+$", ".*$", "^.*"})


class WildcardParameterCheck(BaseCheck):
    """Wildcard Parameter Matching."""

    def metadata(self) -> CheckMetadata:
        meta_path = Path(__file__).with_suffix(".metadata.yaml")
        data = yaml.safe_load(meta_path.read_text())
        return CheckMetadata(**data)

    async def execute(self, snapshot: ServerSnapshot) -> list[Finding]:
        meta = self.metadata()
        findings: list[Finding] = []

        if not snapshot.tools:
            return findings

        for tool in snapshot.tools:
            tool_name: str = tool.get("name", "<unnamed>")
            input_schema: dict | None = tool.get("inputSchema")

            if not input_schema or not isinstance(input_schema, dict):
                continue

            properties: dict = input_schema.get("properties", {})
            if not isinstance(properties, dict):
                continue

            for param_name, param_def in properties.items():
                if not isinstance(param_def, dict):
                    continue

                pattern_val: str = param_def.get("pattern", "")
                if not pattern_val:
                    continue

                if pattern_val.strip() not in _WILDCARD_PATTERNS:
                    continue

                findings.append(
                    Finding(
                        check_id=meta.check_id,
                        check_title=meta.title,
                        status=Status.FAIL,
                        severity=meta.severity,
                        server_name=snapshot.server_name,
                        server_transport=snapshot.transport_type,
                        resource_type="tool",
                        resource_name=f"{tool_name}.{param_name}",
                        status_extended=(
                            f"Tool '{tool_name}' parameter '{param_name}' uses a wildcard "
                            f"pattern ({pattern_val!r}) that matches any input. "
                            f"The pattern provides no meaningful constraint."
                        ),
                        evidence=(
                            f"param={param_name}, pattern={pattern_val!r} (matches any string)"
                        ),
                        remediation=meta.remediation,
                        owasp_mcp=meta.owasp_mcp,
                    )
                )

        if not findings and snapshot.tools:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.PASS,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended=(
                        f"No wildcard-only parameter patterns detected "
                        f"across {len(snapshot.tools)} tool(s)."
                    ),
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )

        return findings
