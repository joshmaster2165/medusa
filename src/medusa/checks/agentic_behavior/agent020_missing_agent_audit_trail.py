"""AGENT-020: Missing Agent Audit Trail.

Checks config for agent action logging configuration. Fails when no
logging-related keys are present in config or environment variables.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

import yaml

from medusa.core.check import BaseCheck, ServerSnapshot
from medusa.core.models import CheckMetadata, Finding, Status
from medusa.utils.patterns.logging_patterns import LOGGING_CONFIG_KEYS, LOGGING_ENV_VARS

AGENT_AUDIT_KEYS = LOGGING_CONFIG_KEYS | {
    "audit_log",
    "audit_trail",
    "action_log",
    "agent_log",
    "tool_log",
    "invocation_log",
}


class MissingAgentAuditTrailCheck(BaseCheck):
    """Missing Agent Audit Trail."""

    def metadata(self) -> CheckMetadata:
        meta_path = Path(__file__).with_suffix(".metadata.yaml")
        data = yaml.safe_load(meta_path.read_text())
        return CheckMetadata(**data)

    async def execute(self, snapshot: ServerSnapshot) -> list[Finding]:
        meta = self.metadata()
        findings: list[Finding] = []

        found_config = _walk_config_for_keys(snapshot.config_raw, AGENT_AUDIT_KEYS)
        found_env = any(k.upper() in LOGGING_ENV_VARS for k in (snapshot.env or {}))

        if not found_config and not found_env:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.FAIL,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended=(
                        f"Server '{snapshot.server_name}' has no agent audit trail "
                        f"or logging configuration."
                    ),
                    evidence="No logging/audit keys in config_raw or environment variables.",
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )
        else:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.PASS,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended="Agent audit trail / logging configuration detected.",
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )

        return findings


def _walk_config_for_keys(config: Any, keys: set[str], _depth: int = 0) -> bool:
    if _depth > 10:
        return False
    if isinstance(config, dict):
        for key in config:
            if isinstance(key, str) and key.lower() in keys:
                return True
            if _walk_config_for_keys(config[key], keys, _depth + 1):
                return True
    elif isinstance(config, list):
        for item in config:
            if _walk_config_for_keys(item, keys, _depth + 1):
                return True
    return False
