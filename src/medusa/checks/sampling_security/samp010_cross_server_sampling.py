"""SAMP-010: Cross-Server Sampling Attack.

Checks if sampling can reach other servers. Fails when sampling is enabled
but no server isolation or cross-server boundary config is detected.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

import yaml

from medusa.core.check import BaseCheck, ServerSnapshot
from medusa.core.models import CheckMetadata, Finding, Status

SERVER_ISOLATION_KEYS: set[str] = {
    "server_isolation",
    "cross_server_restriction",
    "server_boundary",
    "inter_server_sampling_disabled",
    "sampling_server_scope",
    "isolated_sampling",
    "single_server_sampling",
}


class CrossServerSamplingCheck(BaseCheck):
    """Cross-Server Sampling Attack."""

    def metadata(self) -> CheckMetadata:
        meta_path = Path(__file__).with_suffix(".metadata.yaml")
        data = yaml.safe_load(meta_path.read_text())
        return CheckMetadata(**data)

    async def execute(self, snapshot: ServerSnapshot) -> list[Finding]:
        meta = self.metadata()
        findings: list[Finding] = []

        if "sampling" not in snapshot.capabilities:
            return findings

        has_isolation = _walk_config_for_keys(snapshot.config_raw, SERVER_ISOLATION_KEYS)

        if not has_isolation:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.FAIL,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended=(
                        "Sampling is enabled but no cross-server isolation config detected â€” "
                        "sampling may reach other connected MCP servers."
                    ),
                    evidence="sampling in capabilities; no server isolation config.",
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )
        else:
            findings.append(
                Finding(
                    check_id=meta.check_id,
                    check_title=meta.title,
                    status=Status.PASS,
                    severity=meta.severity,
                    server_name=snapshot.server_name,
                    server_transport=snapshot.transport_type,
                    resource_type="server",
                    resource_name=snapshot.server_name,
                    status_extended="Cross-server sampling isolation detected.",
                    remediation=meta.remediation,
                    owasp_mcp=meta.owasp_mcp,
                )
            )

        return findings


def _walk_config_for_keys(config: Any, keys: set[str], _depth: int = 0) -> bool:
    if _depth > 10:
        return False
    if isinstance(config, dict):
        for key in config:
            if isinstance(key, str) and key.lower() in keys:
                return True
            if _walk_config_for_keys(config[key], keys, _depth + 1):
                return True
    elif isinstance(config, list):
        for item in config:
            if _walk_config_for_keys(item, keys, _depth + 1):
                return True
    return False
